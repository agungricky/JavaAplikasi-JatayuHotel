    private void resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetActionPerformed
        koneksi koneksiku=new koneksi();
        koneksiku.koneksi();
        try {
          int pilih=JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Akan Reset Ke default", null, JOptionPane.OK_OPTION);
          if(pilih==JOptionPane.OK_OPTION){
              koneksiku.st.executeUpdate("INSERT INTO data_pengujung VALUES ('111','Admin Jatayu','Root','Root','Admin','123')");
              JOptionPane.showMessageDialog(null, "Data Login Sukses kembali ke default");
        }else{
        JOptionPane.getRootFrame();
        }
        } catch (Exception e) {
            System.out.println("gagal"+e);
            JOptionPane.showMessageDialog(null, "Data default sudah Tersedia Silahkan Login kembali");
        }
    }//GEN-LAST:event_resetActionPerformed

