    private void menulogoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menulogoutActionPerformed
        int pilih=JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Akan LOGOUT ?", null, JOptionPane.OK_OPTION);
        if(pilih==JOptionPane.OK_OPTION){
            JOptionPane.showMessageDialog(null, "Berhasil LOGOUT");
            menulogin.setEnabled(true);
            tools.setEnabled(false);
            menulogout.setEnabled(false);
            toolscmd.setEnabled(false);
        }else{
        JOptionPane.getRootFrame();
         menulogout.setEnabled(true);
        }
    }//GEN-LAST:event_menulogoutActionPerformed

