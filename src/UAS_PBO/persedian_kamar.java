    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        koneksi koneksiku=new koneksi();
        koneksiku.koneksi();
        
        String jk;
        if(kebawah.isSelected()){
            jk=kebawah.getText();
        }
        
        else{
            jk=menengah.getText();
        }
        
        if(keatas.isSelected()){
            jk=keatas.getText();
        }
        try {
            koneksiku.st.executeUpdate("INSERT INTO persedian_kamar VALUE ("
                                     + "'"+blok_kamar.getSelectedItem()+"','"
                                     + ""+nomor.getText()+"','"
                                     + ""+statuskamar.getSelectedItem()+"', '"
                                     + ""+jk+"')");
            tampil_data();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_simpanActionPerformed

