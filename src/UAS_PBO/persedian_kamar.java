    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        koneksi koneksiku=new koneksi();
        koneksiku.koneksi();
        
        String jk;
        if(kebawah.isSelected()){
            jk=kebawah.getText();
        }else{
            jk=menengah.getText();
        }
        if (keatas.isSelected()){
            jk=keatas.getText();
        }
        
        try {
            koneksiku.st.executeUpdate("UPDATE persedian_kamar SET Blok_kamar='"+blok_kamar.getSelectedItem()+"', No_kamar='"+nomor.getText()+"',Status_kamar='"+statuskamar.getSelectedItem()+"',Harga='"+jk+"' WHERE No_kamar='"+nomor.getText()+"'");
            tampil_data();
        } catch (Exception e) {
            System.out.println("Gagal"+e);
        }
    }//GEN-LAST:event_editActionPerformed

