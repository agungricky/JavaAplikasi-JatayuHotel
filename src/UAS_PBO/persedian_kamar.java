    void format_rupiah(){
        try {
             String angka=menengah.getText().replaceAll("\\,", "");
             double ganti=Double.valueOf(angka);
             DecimalFormat df=new DecimalFormat("#,###,##0");
             menengah.setText(df.format(ganti));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }     
    }
    

