    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int baris=tabel.rowAtPoint(evt.getPoint());
        
        String b=tabel.getValueAt(baris, 0).toString(); 
        blok_kamar.setSelectedItem(b);
        
        String no=tabel.getValueAt(baris, 1).toString();
        nomor.setText(no); 
        
        String status=tabel.getValueAt(baris, 2).toString();
        statuskamar.setSelectedItem(status);
        
        String radio=tabel.getValueAt(baris, 3).toString();
        if (radio.equals("150,000")) {
            keatas.setSelected(true);
        }else if (radio.equals("100,000")) {
            menengah.setSelected(true);
        }if (radio.equals("50,000")) {
            kebawah.setSelected(true);
        }
        format_rupiah();
    }//GEN-LAST:event_tabelMouseClicked

