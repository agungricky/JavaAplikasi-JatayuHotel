    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        koneksi koneksiku=new koneksi();
        koneksiku.koneksi();
        
         String jk;
        if (pria.isSelected()) {
            jk=pria.getText();
        }else{
            jk=perempuan.getText();
        }
        
        try {
            koneksiku.st.executeUpdate("UPDATE data_pengujung SET "
                                     + "No_Ktp='"+ktp.getText()+"', "
                                     + "Nama='"+nama.getText()+"',"
                                     + "Alamat='"+alamat.getText()+"', "
                                     + "jenis_kelamin='"+jk+"', "
                                     + "User = NULL "
                                     + "WHERE No_Ktp='"+ktp.getText()+"'");
            tampildata();
        } catch (Exception e) {
            System.out.println("Gagal : "+e);
        }
    }//GEN-LAST:event_editActionPerformed

