    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int baris=tabel.rowAtPoint(evt.getPoint());
        
        String data_ktp=tabel.getValueAt(baris, 0).toString();
        ktp.setText(data_ktp);
        
        String data_nama=tabel.getValueAt(baris, 1).toString();
        nama.setText(data_nama);
        
        String data_alamat=tabel.getValueAt(baris, 2).toString();
        alamat.setText(data_alamat);
        
        String data_jk=tabel.getValueAt(baris, 3).toString();
        if (data_jk.equals("Laki-laki")) {
            pria.setSelected(true);
        }else if (data_jk.equals("Perempuan")) {
            perempuan.setSelected(true);
        }
    }//GEN-LAST:event_tabelMouseClicked

