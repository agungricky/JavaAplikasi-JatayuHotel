    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        koneksi koneksiku=new koneksi();
        koneksiku.koneksi();
        
        String jk;
        if (pria.isSelected()) {
            jk=pria.getText();
        }else{
            jk=perempuan.getText();
        }
         
        try {
            koneksiku.st.executeUpdate("insert into data_pengujung value ("
                                     + "'"+ktp.getText()+"', "
                                     + "'"+nama.getText()+"', "
                                     + "'"+alamat.getText()+"', "
                                     + "'"+jk+"', User='"+NULL+"', '')");
            JOptionPane.showMessageDialog(null, "Berhasil di Simpan");
            tampildata();
            clearActionPerformed(evt);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal di Simpan");
            System.out.println("Gagal : "+e);
        }
    }//GEN-LAST:event_simpanActionPerformed

